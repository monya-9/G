package G2W;

public class Q1 {
	public static void main(String[] args) {
		int a = 20;
		int b = 3;
		int pl = a+b;
		int mi = a-b;
		int mu = a*b;
		int di = a/b;
		int re = a%b;
		
		System.out.println(a + " + "  + b + " = " + pl);
		System.out.println(a + " - "  + b + " = " + mi);
		System.out.println(a + " * "  + b + " = " + mu);
		System.out.println(a + " / "  + b + " = " + di);
		System.out.println(a + " % "  + b + " = " + re);
	}
}
