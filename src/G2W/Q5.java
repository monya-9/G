package G2W;

public class Q5 {
	public static void main(String[] args) {
		int a = 10;
		double b = 3.14;
		double pl = a+b;
		double mi = a-b;
		double mu = a*b;
		double di = a/b;
		
		System.out.println(a + " + "  + b + " = " + (int)pl);
		System.out.println(a + " - "  + b + " = " + (int)mi);
		System.out.println(a + " * "  + b + " = " + (int)mu);
		System.out.println(a + " / "  + b + " = " + (int)di);
	}
}
